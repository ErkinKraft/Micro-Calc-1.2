while True:
    nam1 =int(input("Введите первое число",))
    nam2 =int(input("Введите второе число",))
    nam1 =float(nam1)
    nam2 =float(nam2)
    print("Результат +:", nam1+nam2)
    print("Результат -:", nam1-nam2)
    print("Результат *:", nam1*nam2)
    print("Результат /:", nam1/nam2)
